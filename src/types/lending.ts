// Helper function to get account address as string
export const getAccountAddress = (account: any): string => {
  if (typeof account === 'string') return account;
  if (account && typeof account === 'object') {
    return account.address || account.toString() || '';
  }
  return '';
};

// Helper function to safely convert string/number to number
export const toNumber = (value: string | number | undefined | null): number => {
  if (value === undefined || value === null) return 0;
  const num = Number(value);
  return isNaN(num) ? 0 : num;
};

