const RPC_URL = "https://westend-asset-hub-eth-rpc.polkadot.io"; // Replace with actual URL

const provider = new ethers.providers.JsonRpcProvider(RPC_URL);
const wallet = new ethers.Wallet(process.env.PRIVATE_KEY!, provider);
const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, wallet);

export async function getTokenInfo() {
  const name = await contract.name();
  const symbol = await contract.symbol();
  return { name, symbol };
}

export async function getBalance(address: string) {
  const balance = await contract.balanceOf(address);
  const symbol = await contract.symbol();
  return {
    raw: balance.toString(),
    formatted: ethers.utils.formatUnits(balance, 0),
    symbol,
  };
}

export async function transferTokens(to: string, amount: string) {
  const tx = await contract.transfer(to, ethers.utils.parseUnits(amount, 0));
  await tx.wait();
  return tx.hash;
}

export function getContractInstance() {
  return contract;
}

export function getWalletAddress() {
  return wallet.address;
}
